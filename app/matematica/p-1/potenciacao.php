        <h1>Potenciação</h1>

        <p>A <strong>potenciação</strong> é uma simplificação da forma de expor uma
            multiplicação de fatores iguais. Vejamos um exemplo:</p>
        <p class="equacao">
            3 x 3 = 9 <br>
            3 x 3 x 3 = 27 <br>
            3 x 3 x 3 x 3 = 81</p>
        <p>Nos três exemplos acima, nós estamos multiplicando apenas o número 3. Vejamos agora como
            ficaria a multiplicação repetindo o número 3 dez vezes.</p>

        <p class="equacao">3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 = 59.049</p>

        <p>Para simplificar a notação dessas multiplicações, nós podemos utilizar a
            <strong>potenciação</strong>. Na potenciação, nós representamos apenas uma vez o
            número que será multiplicado e, acima desse número, colocamos a quantidade de vezes que
            ele será repetido. Para os exemplos acima, vejamos como ficará a representação através da
            potenciação:</p>
        <p class="equacao">
            3 x 3 = 3<sup>2</sup> <br>
            3 x 3 x 3 = 3<sup>3</sup> <br>
            3 x 3 x 3 x 3 = 3<sup>4</sup> <br>
            3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 x 3 = 3<sup>10</sup></p>
        <p>Assim como acontece com as demais operações, os termos de uma potência recebem nomes
            específicos:</p>
        <img width="40%" src="https://s1.static.brasilescola.uol.com.br/img/2015/12/termos-potenciacao.jpg" alt="Potenciação">
        <p>A leitura de uma potência também ocorre de uma forma particular. O exemplo acima é lido como
            <strong>“três elevado a dois”</strong>, <strong>“três elevado à segunda potência”</strong> ou,
            mais popularmente, <strong>“três ao quadrado”</strong> ou <strong>“três elevado ao
                quadrado”</strong>. Quando se trata do expoente três, também há uma variação específica.
            A potência pode ser lida como <strong>“elevado ao cubo”</strong>. Apenas os expoentes dois e três possuem
            essas variações, a leitura do restante dos expoentes segue uma mesma ideia. Veja os exemplos a
            seguir:</p>
        <p class="equacao">
            2<sup>4</sup> = “dois elevado a quatro” ou “dois elevado à quarta potência”<br>
            2<sup>5</sup> = “dois elevado a cinco” ou “dois elevado à quinta potência”<br>
            2<sup>6</sup> = “dois elevado a seis” ou “dois elevado à sexta potência”<br>
            2<sup>7</sup> = “dois elevado a sete” ou “dois elevado à sétima potência”<br>
            2<sup>8</sup> = “dois elevado a oito” ou “dois elevado à oitava potência”<br>
            2<sup>9</sup> = “dois elevado a nove” ou “dois elevado à nona potência”<br>
            2<sup>n</sup> = “dois elevado a n” ou “dois elevado à enésima potência”
        </p>
